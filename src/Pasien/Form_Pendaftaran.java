    private void tcariKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tcariKeyPressed
        // TODO add your handling code here:
        DefaultTableModel model= new DefaultTableModel();
        model.addColumn("Id");
        model.addColumn("Nomor");
        model.addColumn("Nama");
        model.addColumn("Alamat");
        model.addColumn("Jenis Kelamin");
        model.addColumn("Umur");
        try {
            String sql = "select * from pasien where id_pasien like '%"+tcari.getText()+"%' or nama like '%"+tcari.getText()+"%' or alamat like '%"+tcari.getText()+"%' or umur like '%"+tcari.getText()+"%'";
            s = conn.createStatement();
            rs = s.executeQuery(sql);

            int no = 0;
            while(rs.next()){
                no++;
                model.addRow(new Object[]{
                    rs.getString("id_pasien"),rs.getString("nomor"),rs.getString("nama"),rs.getString("alamat")
                    ,rs.getString("kelamin"),rs.getString("umur")
                });
            }
            tblpasien.setModel(model);
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_tcariKeyPressed

