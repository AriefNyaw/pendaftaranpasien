    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        // TODO add your handling code here:
        String JK = "";
        //radio button
        if (rlaki.isSelected()){
            JK = "L";
        }else if (rperem.isSelected()){
            JK = "P";
        }
        if (tnomor.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Mohon masukan data untuk di input","PERHATIAN", JOptionPane.WARNING_MESSAGE);
        }else{
            try {
                s = conn.createStatement();
                rs = s.executeQuery("SELECT * FROM pasien WHERE nomor='"+tnomor.getText()+"'or nama='"+tnama.getText()+"'");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(null, "Data sudah Ada coba masukan lagi","PERHATIAN",JOptionPane.WARNING_MESSAGE);
                    tnomor.requestFocus();
                }else{
                    String sql = "insert into pasien values(NULL,'"+tnomor.getText()+"','"+tnama.getText()
                    +"','"+talamat.getText()+"','"+JK+"','"+tumur.getText()+"')";
                    s = conn.createStatement();
                    s.executeUpdate(sql);

                    bersih();
                    tampil();

                    JOptionPane.showMessageDialog(null, "DATA BERHASIL DISIMPAN","SUKSES",JOptionPane.INFORMATION_MESSAGE);
                }
                s.close();
            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
        }
    }//GEN-LAST:event_bsimpanActionPerformed

